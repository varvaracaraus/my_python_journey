# Triangle Area Calculation
# This script calculates the area of a right-angled triangle given the lengths of its two legs.

# Input for the lengths of the triangle's legs
first_leg_length = int(input('Enter the first leg length of the triangle: '))
second_leg_length = int(input('Enter the second leg length of the triangle: '))

# Calculate and print the area of the triangle
area_of_triangle = first_leg_length * second_leg_length / 2
print('Area of the triangle:', area_of_triangle)

